package instance;

public class Car {
    String brand;
    String modelName;
    String color;
    double speedMaximum;

    void setBrand(String brand){
        this.brand = brand;
    }
    void setModelName(String modelName){
        this.modelName = modelName;
    }
    void setColor(String color){
        this.color = color;
    }
    void setSpeedMaximum(double speedMaximum){
        this.speedMaximum = speedMaximum;
    }

}
