package Array;

public class ArrEx {
    public static void main(String[] args) {
        int nArr[] = {1, 2, 3, 4, 5};
        for (int i : nArr) {
            System.out.print(i);
        }
        String strArr[] = {"이현민", "연정훈", "이하준", "윤기진", "김세부", "이제호", "Let's go"};
        for (String member : strArr){
            System.out.println(member);
        }
    }
}
